# Função para obter texto traduzido
def get_text(dictionary, key, lang="pt"):
    """Obtém o texto traduzido do dicionário para o idioma especificado."""
    if lang in dictionary and key in dictionary[lang]:
        return dictionary[lang][key]
    return key  # Retorna a chave como fallback se não encontrar tradução
